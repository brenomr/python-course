# DATA TYPES: BOOLEAN

"""
Annotations:
return one of two options: True or False
some 'operators': ==, !=, is, is not
"""

result1 = 10 == 10
result2 = 10 is 10
result3 = 10 != 10

print(type(result1), end=' ---> ')
print(result1)
print(type(result2), end=' ---> ')
print(result2)
print(type(result3), end=' ---> ')
print(result3)