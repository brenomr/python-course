# MATH operations

addition = 10 + 10
print('Sum:', addition, end='\n'*2)

subtraction = 10 - 3
print('Subtraction:', subtraction, end='\n'*2)

multiplication = 10 * 10
print('Multiplication:', multiplication, end='\n'*2)

integer_division = 10 // 2
print('Integer Division:', integer_division, end='\n'*2)

float_division = 10 / 1.5
print('Float Division:', float_division, end='\n'*2)

exponent = 10 ** 3
print('Exponent:', exponent, end='\n'*2)

remainder = 55 % 2
print('Modulo:', remainder, end='\n'*2)